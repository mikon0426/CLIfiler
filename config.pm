package Config;

use strict;
use warnings;



#===========================================================
# new()
#
# Config クラスのコンストラクタ
#
# ■概要
#   セクション定義順（section_order）を基準に
#   通常値(values) と 配列データ(arrays) を分離して管理する
#
#   ・セクション順序は new() 定義順が絶対
#   ・load() 時のファイル順序は無視される
#   ・save() は必ず定義順で出力される
#
# ■引数
#   $section_list_ref : セクション名の配列リファレンス
#                        (定義順 = 保存順の基準)
#       仕様)
#           ・空配列は不可（最低1セクション必要）
#       役割)
#           ・Config全体の「セクション定義順」を決定する
#           ・この順序が save() の出力順の絶対基準になる
#           ・load() のファイル順は完全に無視される
#       例)
#           [
#               'History',
#               'Favorite',
#               'Bookmark'
#           ]
#
# ■内部構造
#   section_order : セクション定義順配列
#   valid_section : セクション存在チェック用ハッシュ
#   values        : 通常値格納ハッシュ
#   values_order  : 通常値保存順
#   arrays        : 配列データ格納配列
#   array_meta    : 配列制御情報（duplicate / max）
#
# ■配列仕様
#   ・index 0 = 最古
#   ・末尾   = 最新
#   ・push_value() は末尾追加
#   ・max超過時は index 0 を削除
#
# ■配列メタデータ
#   duplicate : デフォルト 0（重複禁止）
#   max       : デフォルト undef（無制限）
#
# ■戻り値
#   Configオブジェクト
#
# ■エラー
#   セクションリストがARRAY refでない場合はdie
#===========================================================
sub new {
    my ($class, $section_list_ref) = @_;

    #---------------------------------------------------
    # 引数チェック
    #---------------------------------------------------
    die "section list required (ARRAY ref)"
        if ref($section_list_ref) ne 'ARRAY';

    #---------------------------------------------------
    # オブジェクト生成
    #---------------------------------------------------
    my $self = bless {

        #===================================================
        # セクション定義順（保存順の基準）
        #===================================================
        section_order => [ @$section_list_ref ],

        #===================================================
        # セクション存在チェック用
        # key => 1
        #===================================================
        valid_section => {},

        #===================================================
        # 通常値データ
        # section => { key => value }
        #===================================================
        values => {},

        #===================================================
        # 通常値保存順
        # section => [ key, key, ... ]
        #===================================================
        values_order => {},

        #===================================================
        # 配列データ
        # section => [ value, value, ... ]
        # index 0 = oldest
        # last   = newest
        #===================================================
        arrays => {},

        #===================================================
        # 配列メタ情報
        # section => {
        #     duplicate => 0/1
        #     max       => undef or int
        # }
        #===================================================
        array_meta => {},

    }, $class;

    #---------------------------------------------------
    # valid_section 初期化
    #---------------------------------------------------
    for my $sec (@{$self->{section_order}}) {

        $self->{valid_section}{$sec} = 1;

        #-----------------------------------------------
        # 初期構造を必ず用意（重要）
        #-----------------------------------------------
        $self->{values}{$sec}       = {};
        $self->{values_order}{$sec} = [];

        $self->{arrays}{$sec}       = [];

        $self->{array_meta}{$sec} = {
            duplicate => 0,     # デフォルト：重複禁止
            max       => undef, # デフォルト：無制限
        };
    }

    return $self;
}



#===========================================================
# set()
#
# 通常値（スカラ値）の設定
#
# ■仕様
#   ・通常値専用（配列キーは使用不可）
#   ・数値キーは禁止（配列と誤認されるため）
#   ・新規キーは values_order に登録される
#   ・既存キー更新時は順序を変更しない
#
# ■引数
#   $section : セクション名
#   $key     : 文字列キー（数値禁止）
#   $value   : 設定値（スカラ）
#
# ■戻り値
#   成功 : 1
#   失敗 : undef
#
# ■影響範囲
#   $self->{values}{$section}{$key}
#   $self->{values_order}{$section}
#
# ■注意
#   ・配列データは push_value / set_array を使用すること
#   ・set_array_duplicate / set_array_max の対象外
#===========================================================
sub set {
    my ($self, $section, $key, $value) = @_;

    #---------------------------------------------------
    # セクションチェック
    #---------------------------------------------------
    return undef
        if !exists $self->{valid_section}{$section};

    #---------------------------------------------------
    # キーチェック（数値禁止）
    #---------------------------------------------------
    if (defined $key && $key =~ /^\d+$/) {
        warn "[set] numeric key is not allowed (use array API)\n";
        return undef;
    }

    #---------------------------------------------------
    # 初期化保証
    #---------------------------------------------------
    $self->{values}{$section}       ||= {};
    $self->{values_order}{$section} ||= [];

    #---------------------------------------------------
    # 新規キー判定
    #---------------------------------------------------
    my $is_new_key =
        !exists $self->{values}{$section}{$key};

    #---------------------------------------------------
    # 値設定
    #---------------------------------------------------
    $self->{values}{$section}{$key} = $value;

    #---------------------------------------------------
    # 保存順登録
    #---------------------------------------------------
    if ($is_new_key) {

        push(
            @{$self->{values_order}{$section}},
            $key
        );
    }

    return 1;
}



#===========================================================
# get()
#
# 通常値（スカラ値）の取得
#
# ■仕様
#   ・通常値専用API
#   ・配列データにはアクセスしない
#   ・数値キーは禁止（set()と同様のルール）
#
# ■引数
#   $section : セクション名
#   $key     : キー（文字列）
#
# ■戻り値
#   成功 : 値（スカラ）
#   失敗 : undef
#
# ■エラー条件
#   ・セクションが存在しない
#   ・キーが存在しない
#
# ■注意
#   ・配列取得は get_array() を使用すること
#===========================================================
sub get {
    my ($self, $section, $key) = @_;

    #---------------------------------------------------
    # セクションチェック
    #---------------------------------------------------
    return undef
        if !exists $self->{valid_section}{$section};

    #---------------------------------------------------
    # values未初期化対策
    #---------------------------------------------------
    return undef
        if !exists $self->{values}{$section};

    #---------------------------------------------------
    # キー存在チェック
    #---------------------------------------------------
    return undef
        if !exists $self->{values}{$section}{$key};

    #---------------------------------------------------
    # 値返却
    #---------------------------------------------------
    return $self->{values}{$section}{$key};
}



#===========================================================
# load()
#
# 設定ファイルの読み込み
#
# ■仕様
#   ・ファイルのセクション順は無視する
#   ・new() で定義されたセクション順が正
#   ・存在しないセクションは warning のみで無視
#   ・通常値と配列値は分離して格納する
#
#   ・配列はそのまま読み込む（整形しない）
#   ・duplicate違反は warning（読み込みは継続）
#
#   ・通常値の重複キーは後勝ち
#   ・保存順は初回出現位置を維持する
#
# ■配列仕様
#   ・インデックスは読み込み順で連番化する
#   ・歯抜け配列は禁止（詰め直す）
#
# ■引数
#   $file : 設定ファイルパス
#
# ■戻り値
#   成功 : 1
#   失敗 : undef
#
#===========================================================
sub load {
    my ($self, $file) = @_;

    open my $fh, '<', $file
        or return undef;

    my $current_section = undef;

    #---------------------------------------------------
    # 配列一時バッファ
    #---------------------------------------------------
    my %array_buffer;

    while (my $line = <$fh>) {

        chomp $line;

        #-----------------------------------------------
        # コメント / 空行スキップ
        #-----------------------------------------------
        next if $line =~ /^\s*$/;
        next if $line =~ /^\s*#/;

        #-----------------------------------------------
        # セクション開始
        #-----------------------------------------------
        if ($line =~ /^\[(.+)\]$/) {

            $current_section = $1;

            #-------------------------------------------
            # セクション存在チェック
            #-------------------------------------------
            if (
                !exists(
                    $self->{valid_section}
                    {$current_section}
                )
            ) {

                warn
                    "[load] unknown section: "
                    . "$current_section\n";

                $current_section = undef;
            }

            next;
        }

        #-----------------------------------------------
        # セクション未定義なら無視
        #-----------------------------------------------
        next
            if !defined $current_section;

        #-----------------------------------------------
        # key=value
        #-----------------------------------------------
        if ($line =~ /^([^=]+)=(.*)$/) {

            my $key   = $1;
            my $value = $2;

            #===========================================
            # 数値キー → 配列扱い
            #===========================================
            if ($key =~ /^\d+$/) {

                push(
                    @{$array_buffer{$current_section}},
                    $value
                );
            }
            else {

                #---------------------------------------
                # 初回出現キー
                #---------------------------------------
                if (
                    !exists(
                        $self->{values}
                        {$current_section}
                        {$key}
                    )
                ) {

                    push(
                        @{$self->{values_order}
                        {$current_section}},
                        $key
                    );
                }

                #---------------------------------------
                # 通常値設定
                # （重複キーは後勝ち）
                #---------------------------------------
                $self->{values}
                    {$current_section}
                    {$key}
                    = $value;
            }
        }
    }

    close $fh;

    #===================================================
    # 配列確定処理
    #===================================================
    for my $sec (keys %array_buffer) {

        next
            if !exists(
                $self->{arrays}{$sec}
            );

        my $idx = 0;

        for my $val (
            @{ $array_buffer{$sec} }
        ) {

            $self->{arrays}{$sec}
                [$idx++] = $val;
        }
    }

    return 1;
}



#===========================================================
# save()
#
# 設定ファイルの保存
#
# ■仕様
#   ・セクション順は new() 定義順のみ使用
#   ・ファイル既存順序は無視する
#   ・空セクションも必ず出力する
#
#   ・通常値 → 配列 の順で出力
#   ・通常値は values_order 順で出力
#   ・配列は index 順（0=old → last=new）
#
# ■注意
#   ・duplicate / max は保存しない（メタ情報のみ保持）
#   ・values_order と values の不整合は warn して継続
#
# ■引数
#   $file : 出力ファイルパス
#
# ■戻り値
#   成功 : 1
#   失敗 : undef
#
#===========================================================
sub save {
    my ($self, $file) = @_;

    open my $fh, '>', $file
        or return undef;

    #---------------------------------------------------
    # セクション順は new() 定義順が絶対
    #---------------------------------------------------
    for my $sec (@{$self->{section_order}}) {

        print $fh "[$sec]\n";

        #===================================================
        # 通常値出力
        #===================================================
        if (
            exists $self->{values}{$sec}
            &&
            exists $self->{values_order}{$sec}
        ) {

            for my $key (
                @{$self->{values_order}{$sec}}
            ) {

                #-------------------------------------------
                # 内部整合性チェック
                #-------------------------------------------
                if (
                    !exists(
                        $self->{values}{$sec}{$key}
                    )
                ) {

                    warn
                        "[save] key '$key' exists in "
                        . "values_order but not in values "
                        . "(section=$sec)\n";

                    next;
                }

                my $val =
                    $self->{values}{$sec}{$key};

                print $fh "$key=$val\n";
            }
        }

        #===================================================
        # 配列出力
        #===================================================
        if (exists $self->{arrays}{$sec}) {

            my $array_ref = $self->{arrays}{$sec};

            for (my $i = 0; $i <= $#$array_ref; $i++) {

                my $val = $array_ref->[$i];

                next
                    if !defined $val;

                print $fh "$i=$val\n";
            }
        }

        print $fh "\n";
    }

    close $fh;

    return 1;
}



#===========================================================
# push_value()
#
# 配列データへの追加（末尾追加＝最新）
#
# ■仕様
#   ・配列末尾に追加（最新データ）
#   ・index 0 = 最古 / last = 最新
#
#   ・duplicate設定に従う
#       0 = 重複禁止（デフォルト）
#       1 = 重複許可
#
#   ・max設定に従う
#       undef = 無制限
#       N     = Nを超えたら古いもの(index 0)を削除
#
# ■影響メソッド
#   set_array_duplicate()
#   set_array_max()
#
# ■引数
#   $section : セクション名
#   $value   : 追加する値
#
# ■戻り値
#   成功 : 1
#   失敗 : undef
#
#===========================================================
sub push_value {
    my ($self, $section, $value) = @_;

    #---------------------------------------------------
    # セクションチェック
    #---------------------------------------------------
    return undef
        if !exists $self->{valid_section}{$section};

    #---------------------------------------------------
    # 配列取得
    #---------------------------------------------------
    my $array_ref = $self->{arrays}{$section};

    return undef
        if ref($array_ref) ne 'ARRAY';

    #---------------------------------------------------
    # メタ情報取得
    #---------------------------------------------------
    my $meta = $self->{array_meta}{$section};

    #===================================================
    # duplicate チェック
    #===================================================
    if (!$meta->{duplicate}) {

        for my $v (@$array_ref) {

            if (defined $v && defined $value && $v eq $value) {

                warn "[push_value] duplicate rejected: $value\n";
                return undef;
            }
        }
    }

    #===================================================
    # 末尾に追加（最新）
    #===================================================
    push @$array_ref, $value;

    #===================================================
    # max 制御（FIFO）
    #===================================================
    if (defined $meta->{max}) {

        while (@$array_ref > $meta->{max}) {

            shift @$array_ref;  # oldest削除
        }
    }

    return 1;
}



#===========================================================
# set_array()
#
# 配列の指定インデックス更新
#
# ■仕様
#   ・配列要素を直接更新する
#   ・push_value() と異なり「追加」ではなく「上書き」
#
# ■重要仕様
#   ・index は必須
#   ・存在しない index はエラー（undef + warn）
#   ・配列の時系列構造を壊す可能性があるため注意
#
#   ・indexの意味
#       0 = 最古
#       last = 最新
#
# ■影響設定
#   set_array_duplicate() の影響は受けない
#   set_array_max() の影響も受けない
#
# ■引数
#   $section : セクション名
#   $index   : 配列インデックス（必須）
#   $value   : 設定値
#
# ■戻り値
#   成功 : 1
#   失敗 : undef
#
#===========================================================
sub set_array {
    my ($self, $section, $index, $value) = @_;

    #---------------------------------------------------
    # セクションチェック
    #---------------------------------------------------
    return undef
        if !exists $self->{valid_section}{$section};

    #---------------------------------------------------
    # 配列チェック
    #---------------------------------------------------
    my $array_ref = $self->{arrays}{$section};

    return undef
        if ref($array_ref) ne 'ARRAY';

    #---------------------------------------------------
    # indexチェック
    #---------------------------------------------------
    if (!defined $index || $index !~ /^\d+$/) {

        warn "[set_array] invalid index\n";
        return undef;
    }

    #---------------------------------------------------
    # 存在しないindexはエラー
    #---------------------------------------------------
    if ($index < 0 || $index > $#$array_ref) {

        warn "[set_array] index out of range: $index\n";
        return undef;
    }

    #---------------------------------------------------
    # 上書き処理
    #---------------------------------------------------
    $array_ref->[$index] = $value;

    return 1;
}



#===========================================================
# get_array()
#
# 配列データの取得
#
# ■仕様
#   ・配列全体または単一要素を取得する
#
#   ・index省略時：
#       配列リファレンスを返す（内部データそのまま）
#
#   ・index指定時：
#       指定要素をスカラで返す
#
# ■index仕様
#   ・0 = 最古
#   ・last = 最新
#
# ■エラー仕様
#   ・セクションが存在しない場合：undef
#   ・indexが範囲外の場合：undef
#
# ■引数
#   $section : セクション名
#   $index   : 配列インデックス（省略可）
#
# ■注意
#   ・戻り値は内部配列参照のため直接変更は非推奨
#   ・変更は set_array / push_value を使用すること
#
#===========================================================
sub get_array {
    my ($self, $section, $index) = @_;

    #---------------------------------------------------
    # セクションチェック
    #---------------------------------------------------
    return undef
        if !exists $self->{valid_section}{$section};

    #---------------------------------------------------
    # 配列取得
    #---------------------------------------------------
    my $array_ref = $self->{arrays}{$section};

    return undef
        if ref($array_ref) ne 'ARRAY';

    #===================================================
    # index未指定 → 配列リファレンス返却
    #===================================================
    if (!defined $index) {
        return $array_ref;
    }

    #---------------------------------------------------
    # indexチェック
    #---------------------------------------------------
    if ($index !~ /^\d+$/) {
        warn "[get_array] invalid index\n";
        return undef;
    }

    #---------------------------------------------------
    # 範囲チェック
    #---------------------------------------------------
    if ($index < 0 || $index > $#$array_ref) {
        return undef;
    }

    #---------------------------------------------------
    # 要素返却
    #---------------------------------------------------
    return $array_ref->[$index];
}



#===========================================================
# get_array_size()
#
# 配列サイズの取得
#
# ■仕様
#   ・指定セクションの配列要素数を返す
#   ・indexや値の内容には影響しない
#
# ■配列仕様
#   ・0 = 最古
#   ・last = 最新
#
# ■エラー仕様
#   ・セクションが存在しない場合：undef
#   ・配列が未初期化の場合：undef
#
# ■引数
#   $section : セクション名
#
# ■戻り値
#   成功 : 要素数（整数）
#   失敗 : undef
#
#===========================================================
sub get_array_size {
    my ($self, $section) = @_;

    #---------------------------------------------------
    # セクションチェック
    #---------------------------------------------------
    return undef
        if !exists $self->{valid_section}{$section};

    #---------------------------------------------------
    # 配列チェック
    #---------------------------------------------------
    my $array_ref = $self->{arrays}{$section};

    return undef
        if ref($array_ref) ne 'ARRAY';

    #---------------------------------------------------
    # サイズ取得
    #---------------------------------------------------
    return scalar @$array_ref;
}



#===========================================================
# _validate_config()
#
# セクション整合性チェック（内部関数）
#
# History:
#   duplicateチェックは dir を使用
#
# ■引数
#   $section : セクション名
#
# ■戻り値
#    0 : 問題なし
#   -1 : duplicate違反
#   -2 : max超過
#   -3 : duplicate違反 + max超過
#
#===========================================================
sub _validate_config {
    my ($self, $section) = @_;

    my $dup_error = 0;
    my $max_error = 0;

    my $arr_ref =
        $self->{arrays}{$section};

    my $duplicate = 0;
    my $max       = undef;

    if(
        exists(
            $self->{array_meta}{$section}
        )
    ) {

        if(
            exists(
                $self->{array_meta}{$section}{duplicate}
            )
        ) {

            $duplicate =
                $self->{array_meta}{$section}{duplicate};
        }

        if(
            exists(
                $self->{array_meta}{$section}{max}
            )
        ) {

            $max =
                $self->{array_meta}{$section}{max};
        }
    }

    #---------------------------------------------------
    # duplicate
    #---------------------------------------------------

    if(
        !$duplicate
    ) {

        my %seen;

        if(
            $section eq 'History'
        ) {

            foreach my $value (@{$arr_ref}) {

                my ($dir) =
                    split(
                        /\|/,
                        $value,
                        2
                    );

                if(
                    $seen{$dir}++
                ) {

                    $dup_error = 1;

                    last;
                }
            }
        }
        else {

            foreach my $value (@{$arr_ref}) {

                if(
                    $seen{$value}++
                ) {

                    $dup_error = 1;

                    last;
                }
            }
        }
    }

    #---------------------------------------------------
    # max
    #---------------------------------------------------

    if(
        defined($max)
    ) {

        my $size =
            scalar(
                @{$arr_ref}
            );

        if(
            $size > $max
        ) {

            $max_error = 1;
        }
    }

    #---------------------------------------------------
    # return
    #---------------------------------------------------

    if(
        $dup_error
        &&
        $max_error
    ) {

        return -3;
    }

    if(
        $dup_error
    ) {

        return -1;
    }

    if(
        $max_error
    ) {

        return -2;
    }

    return 0;
}



#===========================================================
# set_array_duplicate()
#
# 配列の重複許可設定
#
# ■引数
#   $section   : セクション名
#   $duplicate : 0=重複禁止
#                1=重複許可
#
# ■戻り値
#    0 : 問題なし
#   -1 : duplicate違反
#   -2 : max超過
#   -3 : duplicate違反 + max超過
#
#===========================================================
sub set_array_duplicate {
    my ($self, $section, $duplicate) = @_;

    #---------------------------------------------------
    # section check
    #---------------------------------------------------
    if(!exists($self->{arrays}{$section})) {
        warn( "Config: unknown section ". "'$section'\n" );
        return undef;
    }

    $duplicate = $duplicate ? 1 : 0;

    #---------------------------------------------------
    # duplicate設定
    #---------------------------------------------------
    $self->{array_meta}{$section}{duplicate}
        = $duplicate;

    #---------------------------------------------------
    # validation
    #---------------------------------------------------
    my $ret =
        $self->_validate_config(
            $section
        );

    if(
        $ret != 0
    ) {

        warn(
            "Config: section '$section' "
            . "validation error ($ret)\n"
        );
    }

    return $ret;
}



#===========================================================
# set_array_max()
#
# 配列最大数設定
#
# ■引数
#   $section : セクション名
#   $max     : 最大要素数
#
# ■戻り値
#    0 : 問題なし
#   -1 : duplicate違反
#   -2 : max超過
#   -3 : duplicate違反 + max超過
#
#===========================================================
sub set_array_max {
    my ($self, $section, $max) = @_;

    #---------------------------------------------------
    # section check
    #---------------------------------------------------
    if(!exists($self->{arrays}{$section})) {
        warn( "Config: unknown section ". "'$section'\n" );
        return undef;
    }

    #---------------------------------------------------
    # max設定
    #---------------------------------------------------
    $self->{array_meta}{$section}{max}
        = $max;

    #---------------------------------------------------
    # validation
    #---------------------------------------------------
    my $ret =
        $self->_validate_config(
            $section
        );

    if(
        $ret != 0
    ) {

        warn(
            "Config: section '$section' "
            . "validation error ($ret)\n"
        );
    }

    return $ret;
}



#===========================================================
# push_history_value()
#
# Historyへ履歴追加
#
# ■引数
#   $dir    : ディレクトリまたはファイル名
#   $loc    : カーソル位置
#   $offset : カーソル位置オフセット
#
# ■戻り値
#   1     : 成功
#   undef : 失敗
#
#===========================================================
sub push_history_value {
    my ($self, $dir, $loc, $offset) = @_;

    return undef
        if(
            !defined($dir)
            ||
            $dir eq ''
        );

    my $arr_ref =
        $self->{arrays}{History};

    my $value =
        join(
            '|',
            $dir,
            $loc,
            $offset
        );

    my $duplicate = 0;

    if(
        exists(
            $self->{array_meta}{History}
        )
        &&
        exists(
            $self->{array_meta}{History}{duplicate}
        )
    ) {

        $duplicate =
            $self->{array_meta}{History}{duplicate};
    }

    #---------------------------------------------------
    # duplicate = 0
    # 同じdirを削除
    #---------------------------------------------------

    if(
        !$duplicate
    ) {

        my @new_array;

        foreach my $item (@{$arr_ref}) {

            my ($item_dir) =
                split(
                    /\|/,
                    $item,
                    2
                );

            next
                if(
                    $item_dir eq $dir
                );

            push(
                @new_array,
                $item
            );
        }

        $self->{arrays}{History} =
            \@new_array;

        $arr_ref =
            $self->{arrays}{History};
    }

    #---------------------------------------------------
    # 新しい履歴追加
    #---------------------------------------------------

    push(
        @{$arr_ref},
        $value
    );

    #---------------------------------------------------
    # max制限
    #---------------------------------------------------

    if(
        exists(
            $self->{array_meta}{History}
        )
        &&
        exists(
            $self->{array_meta}{History}{max}
        )
        &&
        defined(
            $self->{array_meta}{History}{max}
        )
    ) {

        my $max =
            $self->{array_meta}{History}{max};

        while(
            scalar(@{$arr_ref}) > $max
        ) {

            shift(
                @{$arr_ref}
            );
        }
    }

    return 1;
}


#===========================================================
# dump()
#
# Config内部構造のダンプ出力（デバッグ用）
#
# ■概要
#   ・現在保持している全データを可視化する
#   ・values / arrays / メタ情報 / セクション順を出力
#
# ■出力順
#   1. section_order
#   2. values
#   3. arrays
#   4. array_meta
#
# ■仕様
#   ・読み取り専用（状態変更なし）
#   ・空セクションも出力する
#   ・デバッグ用途前提
#
# ■引数
#   なし
#
# ■戻り値
#   成功 : 1
#   失敗 : undef
#
#===========================================================
sub dump {
    my ($self) = @_;

    #---------------------------------------------------
    # section_order
    #---------------------------------------------------
    print "====================\n";
    print "section_order\n";
    print "====================\n";

    for my $sec (@{$self->{section_order}}) {
        print "$sec\n";
    }

    print "\n";

    #---------------------------------------------------
    # values
    #---------------------------------------------------
    print "====================\n";
    print "values\n";
    print "====================\n";

    for my $sec (@{$self->{section_order}}) {

        print "[$sec]\n";

        if (exists $self->{values}{$sec}) {

            for my $k (keys %{ $self->{values}{$sec} }) {

                my $v = $self->{values}{$sec}{$k};
                print "  $k = $v\n";
            }
        }

        print "\n";
    }

    #---------------------------------------------------
    # arrays
    #---------------------------------------------------
    print "====================\n";
    print "arrays\n";
    print "====================\n";

    for my $sec (@{$self->{section_order}}) {

        print "[$sec]\n";

        if (exists $self->{arrays}{$sec}) {

            my $arr = $self->{arrays}{$sec};

            for (my $i = 0; $i <= $#$arr; $i++) {

                my $v = $arr->[$i];

                if (ref($v) eq 'HASH') {

                    # push_history_value 用データ
                    my $dir    = $v->{dir}    // '';
                    my $loc    = $v->{loc}    // '';
                    my $offset = $v->{offset} // '';

                    print "  $i = {dir:$dir, loc:$loc, offset:$offset}\n";
                }
                else {
                    print "  $i = $v\n";
                }
            }
        }

        print "\n";
    }

    #---------------------------------------------------
    # array_meta
    #---------------------------------------------------
    print "====================\n";
    print "array_meta\n";
    print "====================\n";

    for my $sec (@{$self->{section_order}}) {

        print "[$sec]\n";

        if (exists $self->{array_meta}{$sec}) {

            my $m = $self->{array_meta}{$sec};

            my $dup = $m->{duplicate} // 0;
            my $max = defined $m->{max} ? $m->{max} : 'undef';

            print "  duplicate = $dup\n";
            print "  max       = $max\n";
        }

        print "\n";
    }

    return 1;
}



#===========================================================
# clear_section()
#
# セクション内データの初期化
#
# ■概要
#   ・指定セクションのデータをすべてクリアする
#   ・ただしセクション構造（枠）は維持する
#
# ■重要仕様
#   ・values と arrays のみ初期化する
#   ・array_meta（duplicate / max）は保持する
#   ・section自体は削除しない
#
#   → 設定（振る舞い）は維持し、データのみリセット
#
# ■初期化対象
#   ・values{$section} = {}
#   ・arrays{$section} = []
#
# ■保持対象
#   ・array_meta{$section}（duplicate / max）
#   ・valid_section
#   ・section_order
#
# ■引数
#   $section : セクション名
#
# ■戻り値
#   成功 : 1
#   失敗 : undef
#
#===========================================================
sub clear_section {
    my ($self, $section) = @_;

    #---------------------------------------------------
    # セクションチェック
    #---------------------------------------------------
    return undef
        if !exists $self->{valid_section}{$section};

    #---------------------------------------------------
    # values 初期化
    #---------------------------------------------------
    $self->{values}{$section} = {};

    #---------------------------------------------------
    # arrays 初期化
    #---------------------------------------------------
    $self->{arrays}{$section} = [];

    #---------------------------------------------------
    # 注意：
    # array_meta は保持する（重要）
    #---------------------------------------------------

    return 1;
}



